-- ScriptSignal implementation
local function newSignal()
	local connected = {}
	return {
		Connect = function(self, f)
			connected[tostring(f)] = f
			return {
				Connected = true,
				Disconnect = function()
					connected[tostring(f)] = nil
				end,
			}
		end,
		Fire = function(self, ...)
			for _, f in pairs(connected) do
				coroutine.wrap(f)(...)
			end
		end,
	}
end

