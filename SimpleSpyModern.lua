-- Value to string conversion
local function formatstr(str)
	return '"' .. string.gsub(str, '"', '\\226\\128\\162') .. '"'
end

local function v2s(v)
	if typeof(v) == "number" then
		if v == math.huge then return "math.huge"
		elseif tostring(v):match("nan") then return "0/0 --[[NaN]]"
		else return tostring(v) end
	elseif typeof(v) == "boolean" then
		return tostring(v)
	elseif typeof(v) == "string" then
		return formatstr(v)
	elseif typeof(v) == "Instance" then
		if v == nil then return "nil" end
		return "game." .. v:GetFullName()
	elseif typeof(v) == "table" then
		local s = "{"
		for k, val in pairs(v) do
			s = s .. "\n    [" .. v2s(k) .. "] = " .. v2s(val) .. ","
		end
		return s .. "\n}"
	else
		return "nil --[[" .. typeof(v) .. "]]"
	end
end

local function genScript(remote, args)
	local gen = "-- Script generated by SimpleSpy\n\n"
	if #args > 0 then
		gen = gen .. "local args = {"
		for i, v in pairs(args) do
			gen = gen .. "\n    [" .. i .. "] = " .. v2s(v) .. ","
		end
		gen = gen .. "\n}\n\n"
		if remote:IsA("RemoteEvent") then
			gen = gen .. v2s(remote) .. ":FireServer(unpack(args))"
		elseif remote:IsA("RemoteFunction") then
			gen = gen .. v2s(remote) .. ":InvokeServer(unpack(args))"
		end
	else
		if remote:IsA("RemoteEvent") then
			gen = gen .. v2s(remote) .. ":FireServer()"
		elseif remote:IsA("RemoteFunction") then
			gen = gen .. v2s(remote) .. ":InvokeServer()"
		end
	end
	return gen
end

