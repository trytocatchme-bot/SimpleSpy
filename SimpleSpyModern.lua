-- Remote logging function
local function addRemote(type, name, args, remote, blocked)
	if blacklist[remote] or blacklist[name] then return end
	
	local remoteFrame = RemoteTemplate:Clone()
	remoteFrame.Text.Text = string.sub(name, 1, 50)
	remoteFrame.ColorBar.BackgroundColor3 = type == "event" and Color3.new(255, 242, 0) or Color3.fromRGB(99, 86, 245)
	remoteFrame.LayoutOrder = layoutOrderNum
	layoutOrderNum = layoutOrderNum - 1
	
	local log = {
		Name = name,
		Remote = remote,
		Args = args,
		Blocked = blocked,
		Log = remoteFrame,
		GenScript = blocked and "-- THIS REMOTE WAS BLOCKED\n\n" .. genScript(remote, args) or genScript(remote, args),
	}
	
	logs[#logs + 1] = log
	
	remoteFrame.Button.MouseButton1Click:Connect(function()
		if selected and selected.Log then
			TweenService:Create(selected.Log.Button, TweenInfo.new(0.3), {BackgroundColor3 = Color3.new(0, 0, 0)}):Play()
		end
		selected = log
		if selected.Log then
			TweenService:Create(selected.Log.Button, TweenInfo.new(0.3), {BackgroundColor3 = Color3.fromRGB(92, 126, 229)}):Play()
			TextBox.Text = selected.GenScript
		end
		if sideClosed then
			sideClosed = false
			TweenService:Create(RightPanel, TweenInfo.new(0.3), {Size = UDim2.new(0, 319, 0, 249)}):Play()
		end
	end)
	
	remoteFrame.Parent = LogList
	table.insert(remoteLogs, {remoteFrame})
	
	LogList.CanvasSize = UDim2.new(0, 0, 0, UIListLayout.AbsoluteContentSize.Y)
end

-- Hook setup with detour pattern (more reliable)
local originalNamecall

local function setupHooks()
	if not hookmetamethod or not getnamecallmethod then
		error("hookmetamethod/getnamecallmethod non disponibili! Questa funzione richiede un executor moderno (Synapse X, Krampus, etc.)")
	end
	
	originalNamecall = hookmetamethod(game, "__namecall", function(self, ...)
		local methodName = getnamecallmethod()
		local args = {...}
		
		if typeof(self) == "Instance" and (methodName == "FireServer" or methodName == "InvokeServer") then
			local name = self.Name
			
			-- Fire signal for API users
			if remoteSignals[self] then
				remoteSignals[self]:Fire(methodName, args)
			end
			
			-- Add to logs (non-blocking)
			coroutine.wrap(function()
				addRemote(methodName == "FireServer" and "event" or "function", name, args, self, blocklist[self] or blocklist[name])
			end)()
			
			-- Check blocklist - prevent firing
			if blocklist[self] or blocklist[name] then
				return nil
			end
			
			-- Check for hooks
			if remoteHooks[self] then
				local hookedArgs = remoteHooks[self](unpack(args))
				if hookedArgs then
					return originalNamecall(self, unpack(hookedArgs))
				end
			end
		end
		
		return originalNamecall(self, ...)
	end)
	
	return originalNamecall
end

-- SimpleSpy API
local SimpleSpy = {}

function SimpleSpy:GetRemoteFiredSignal(remote)
	assert(typeof(remote) == "Instance", "Instance expected, got " .. typeof(remote))
	if not remoteSignals[remote] then
		remoteSignals[remote] = newSignal()
	end
	return remoteSignals[remote]
end

function SimpleSpy:HookRemote(remote, callback)
	assert(typeof(remote) == "Instance", "Instance expected, got " .. typeof(remote))
	assert(typeof(callback) == "function", "function expected, got " .. typeof(callback))
	remoteHooks[remote] = callback
end

function SimpleSpy:BlockRemote(remote)
	assert(typeof(remote) == "Instance" or typeof(remote) == "string", "Instance | string expected, got " .. typeof(remote))
	blocklist[remote] = true
end

function SimpleSpy:ExcludeRemote(remote)
	assert(typeof(remote) == "Instance" or typeof(remote) == "string", "Instance | string expected, got " .. typeof(remote))
	blacklist[remote] = true
end

function SimpleSpy:ValueToString(value)
	return v2s(value)
end

function SimpleSpy:TableToVars(t)
	assert(typeof(t) == "table", "table expected, got " .. typeof(t))
	return v2s(t)
end

function SimpleSpy:ArgsToString(method, args)
	assert(typeof(method) == "string", "string expected, got " .. typeof(method))
	assert(typeof(args) == "table", "table expected, got " .. typeof(args))
	return v2s({args = args}) .. "\n\n" .. method .. "(unpack(args))"
end

