-- Logging function
local function addRemote(type, name, args, remote, blocked)
	if blacklist[remote] or blacklist[name] then return end
	
	local entry = Template:Clone()
	entry.Visible = true
	entry.Name = name .. "_" .. tick()
	entry.LayoutOrder = layoutOrderNum
	layoutOrderNum = layoutOrderNum - 1
	
	local log = {
		Name = name,
		Remote = remote,
		Args = args,
		Blocked = blocked,
		GenScript = blocked and "-- REMOTE BLOCKED\n\n" .. genScript(remote, args) or genScript(remote, args),
	}
	
	logs[#logs + 1] = log
	
	entry.MouseButton1Click:Connect(function()
		if selected then
			TweenService:Create(selected.Frame, TweenInfo.new(0.2), {BackgroundColor3 = Color3.fromRGB(80, 80, 85)}):Play()
		end
		selected = log
		log.Frame = entry
		TweenService:Create(entry, TweenInfo.new(0.2), {BackgroundColor3 = Color3.fromRGB(92, 126, 229)}):Play()
		CodeBox.Text = log.GenScript
		StatusLabel.Text = "Remote: " .. name
		if sideClosed then
			sideClosed = false
			RightPanel.Size = UDim2.new(0, 300, 0, 275)
		end
	end)
	
	entry.Parent = LogList
	table.insert(remoteLogs, entry)
	
	LogList.CanvasSize = UDim2.new(0, 0, 0, UIListLayout.AbsoluteContentSize.Y + 10)
	TemplateText.Text = string.sub(name, 1, 40)
end

-- Setup hooks
local function setupHooks()
	-- Try hookmetamethod first (Synapse X, Krampus, etc)
	if hookmetamethod and getnamecallmethod then
		local old = hookmetamethod(game, "__namecall", function(self, ...)
			local method = getnamecallmethod()
			local args = {...}
			
			if typeof(self) == "Instance" and (method == "FireServer" or method == "InvokeServer") then
				local name = self.Name
				
				-- Fire signal
				if remoteSignals[self] then
					remoteSignals[self]:Fire(method, args)
				end
				
				-- Apply hooks
				if remoteHooks[self] then
					args = remoteHooks[self](unpack(args))
					if not args then args = {} end
				end
				
				-- Check blocklist
				if blocklist[self] or blocklist[name] then
					spawn(function() addRemote(method == "FireServer" and "event" or "function", name, args, self, true) end)
					return nil
				end
				
				-- Log remote
				spawn(function() addRemote(method == "FireServer" and "event" or "function", name, args, self, false) end)
			end
			
			return old(self, ...)
		end)
		
		if old then
			hookSuccess = true
			_G.SimpleSpyHookBackup = old
			return true
		end
	end
	
	-- Fallback to getrawmetatable (deprecated executors)
	if getrawmetatable and setreadonly then
		local mt = getrawmetatable(game)
		if mt and mt.__namecall then
			_G.SimpleSpyOriginalNamecall = mt.__namecall
			setreadonly(mt, false)
			mt.__namecall = function(self, ...)
				local method = getnamecallmethod and getnamecallmethod() or ""
				local args = {...}
				
				if typeof(self) == "Instance" and (method == "FireServer" or method == "InvokeServer") then
					local name = self.Name
					
					if remoteSignals[self] then
						remoteSignals[self]:Fire(method, args)
					end
					
					if remoteHooks[self] then
						args = remoteHooks[self](unpack(args))
						if not args then args = {} end
					end
					
					if blocklist[self] or blocklist[name] then
						spawn(function() addRemote(method == "FireServer" and "event" or "function", name, args, self, true) end)
						return nil
					end
					
					spawn(function() addRemote(method == "FireServer" and "event" or "function", name, args, self, false) end)
				end
				
				return _G.SimpleSpyOriginalNamecall(self, ...)
			end
			setreadonly(mt, true)
			hookSuccess = true
			return true
		end
	end
	
	return false
end

-- API
local SimpleSpy = {}

function SimpleSpy:GetRemoteFiredSignal(remote)
	assert(typeof(remote) == "Instance", "Instance expected, got " .. typeof(remote))
	remoteSignals[remote] = remoteSignals[remote] or newSignal()
	return remoteSignals[remote]
end

function SimpleSpy:HookRemote(remote, callback)
	assert(typeof(remote) == "Instance", "Instance expected")
	assert(typeof(callback) == "function", "function expected")
	remoteHooks[remote] = callback
end

function SimpleSpy:BlockRemote(remote)
	assert(typeof(remote) == "Instance" or typeof(remote) == "string", "Instance or string expected")
	blocklist[remote] = true
end

function SimpleSpy:ExcludeRemote(remote)
	assert(typeof(remote) == "Instance" or typeof(remote) == "string", "Instance or string expected")
	blacklist[remote] = true
end

function SimpleSpy:ValueToString(value) return v2s(value) end
function SimpleSpy:TableToVars(t) return v2s(t) end
function SimpleSpy:ArgsToString(method, args) return v2s({args = args}) .. "\n\n" .. method .. "(unpack(args))" end

